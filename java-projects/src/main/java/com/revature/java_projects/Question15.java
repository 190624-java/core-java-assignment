package com.revature.java_projects;

public class Question15 implements MathInter {

	public float add(float firstNum, float secondNum) 
	{
		return firstNum + secondNum;
	}

	public float subtract(float firstNum, float secondNum) 
	{
		return firstNum - secondNum;
	}

	public float multiply(float firstNum, float secondNum) 
	{
		return firstNum * secondNum;
	}

	public float divide(float firstNum, float secondNum) 
	{
		return firstNum / secondNum;
	}

}
